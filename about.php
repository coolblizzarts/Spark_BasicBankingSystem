            <h1>History of YE (Youth Empowerment) Bank</h1>
            <p class="text-grey mb-4">TSF Bank was established on December 10, 1970, with a registered office in Kolkata and a corporate office in Mumbai. The Administrator of the The Spark Foundation (TSF), Life Insurance Corporation of India (LIC), General Insurance Corporation, National Insurance Company, The New India Assurance Company, The Oriental Insurance Corporation, and United India Insurance Company all worked together to establish the bank. Manmohan Singh, India's then-finance minister, opened the first branch in Kolkata on July 12, 1971.
              <br>TSF Bank agreed to combine with Global Trust Bank in 1988, but the Reserve Bank of India (RBI) refused to approve the transaction, and the merger did not take place. In 1991, the RBI imposed a ban on Global Trust and oversaw its merger with Oriental Bank of Commerce. TSF Bank was listed on the London Stock Exchange the following year. In the year 1993, TSF Bank opened its first overseas branch in Singapore. The same year it opened an office in Shanghai, China. In 2007, it opened a branch in the Dubai International Financial Centre and branches in Hong Kong.
